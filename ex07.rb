Why are you using the variable named 'snow'?
That's actually not a variable: it is just a string with the word snow in it. A variable wouldn't have the single-quotes around it.

Is it normal to write an English comment for every line of code like you say to do in Study Drill 1?
No, you write comments only to explain difficult to understand code or why you did something. Why is usually much more important, and then you try to write the code so that it explains how something is being done on its own. However, sometimes you have to write such nasty code to solve a problem that it does need a comment on every line. In this case it's strictly for you practice translating code to English.

Can I use single-quotes or double-quotes to make a string or do they do different things?
In Ruby the " (double-quote) tell Ruby to replace variables it finds with #{}, but the ' (single-quote) tells Ruby to leave the string alone and ignore any vraibles inside it.

